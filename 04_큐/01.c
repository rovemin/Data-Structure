/* 선형큐 */

#include <stdio.h>
#include <stdlib.h>
#define MAX_QUEUE_SIZE 5

typedef int element;    //큐의 요소 타입
typedef struct {        //큐 타입
    int front;
    int rear;
    element data[MAX_QUEUE_SIZE];
} QueueType;

void error(char* message)   //오류 함수
{
    fprintf(stderr, "%s\n", message);
    exit(1);
}

void init_queue(QueueType* q)   //큐 초기화
{
    q->rear = -1;
    q->front = -1;
}

void queue_print(QueueType* q)  //큐의 출력
{
    for (int i=0; i<MAX_QUEUE_SIZE; i++) {
        if (i <= q->front || i> q->rear)
            printf("    | ");
        else
            printf("%d  | ", q->data[i]);
    }
    printf("\n");
}

int is_full(QueueType* q)   //큐에 빈 공간이 없나?
{
    if (q->rear == MAX_QUEUE_SIZE - 1)
        return 1;
    else
        return 0;
}

int is_empty(QueueType* q)  //큐가 비었는가?
{
    if (q->front == q->rear)
        return 1;
    else
        return 0;
}

void enqueue(QueueType* q, int item)  //큐에 삽입
{
    if (is_full(q)) {
        error("큐가 포화상태입니다.");
        return;
    }
    q->data[++(q->rear)] = item;
}

int dequeue(QueueType* q)   //큐에서 삭제
{
    if (is_empty(q)) {
        error("큐가 공백상태입니다.");
        return -1;
    }
    int item = q->data[++(q->front)];
    return item;
}

int main(void){
    int item = 0;
    QueueType q;    //큐 생성

    init_queue(&q); //큐의 초기화

    enqueue(&q, 10);    queue_print(&q);    //큐에 삽입
    enqueue(&q, 20);    queue_print(&q);
    enqueue(&q, 30);    queue_print(&q);

    item = dequeue(&q); queue_print(&q);    //큐에서 삭제
    item = dequeue(&q); queue_print(&q);
    item = dequeue(&q); queue_print(&q);

    return 0;
}